// List of countries that require HMRC number (UK and Sweden)
export const HMRC_COUNTRY_CODES = ["GB", "SE"];

// Function to check if a country is in the EU by its country code
export function isEUCountry(countryCode: string): boolean {
  // Handle undefined, null, or empty strings
  if (!countryCode) {
    console.warn('isEUCountry called with empty country code');
    return false;
  }
  
  // Convert to uppercase for case-insensitive comparison
  return EU_COUNTRY_CODES.includes(countryCode.toUpperCase());
}

// Function to check if a country requires HMRC number (UK and Sweden)
export function isHMRCCountry(countryCode: string): boolean {
  // Handle undefined, null, or empty strings
  if (!countryCode) {
    console.warn('isHMRCCountry called with empty country code');
    return false;
  }
  
  // Convert to uppercase for case-insensitive comparison
  return HMRC_COUNTRY_CODES.includes(countryCode.toUpperCase());
}