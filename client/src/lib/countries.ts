// Function to get country code from country name
export function getCountryCodeByName(countryName: string): string | undefined {
  const country = COUNTRIES.find(c => c.name === countryName);
  return country?.code;
}

// Function to get country name from country code
export function getCountryNameByCode(countryCode: string): string | undefined {
  const country = COUNTRIES.find(c => c.code === countryCode);
  return country?.name;
}

// Function to get states/provinces for a country code
export function getStatesByCountryCode(countryCode: string): { name: string, code: string }[] {
  return STATES[countryCode as keyof typeof STATES] || [];
}

// Function to check if a country has states/provinces
export function hasStates(countryCode: string): boolean {
  return !!STATES[countryCode as keyof typeof STATES];
}

